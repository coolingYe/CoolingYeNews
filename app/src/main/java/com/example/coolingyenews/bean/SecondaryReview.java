package com.example.coolingyenews.bean;

public class SecondaryReview {
    private int rid;
    private int uid;
    private String time;
    private String text;
    private String uname;
    private String f_rid;
    private String f_uname;
    private String h_rid;
    private String h_uname;
    private String level;

    public int getRid() {
        return rid;
    }

    public void setRid(int rid) {
        this.rid = rid;
    }

    public int getUid() {
        return uid;
    }

    public void setUid(int uid) {
        this.uid = uid;
    }

    public String getTime() {
        return time;
    }

    public void setTime(String time) {
        this.time = time;
    }

    public String getText() {
        return text;
    }

    public void setText(String text) {
        this.text = text;
    }

    public String getUname() {
        return uname;
    }

    public void setUname(String uname) {
        this.uname = uname;
    }

    public String getF_rid() {
        return f_rid;
    }

    public void setF_rid(String f_rid) {
        this.f_rid = f_rid;
    }

    public String getF_uname() {
        return f_uname;
    }

    public void setF_uname(String f_uname) {
        this.f_uname = f_uname;
    }

    public String getH_rid() {
        return h_rid;
    }

    public void setH_rid(String h_rid) {
        this.h_rid = h_rid;
    }

    public String getH_uname() {
        return h_uname;
    }

    public void setH_uname(String h_uname) {
        this.h_uname = h_uname;
    }

    public String getLevel() {
        return level;
    }

    public void setLevel(String level) {
        this.level = level;
    }
}
